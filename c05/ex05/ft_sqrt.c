int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (nb > 0)
	{
		nb = nb - j;
		j += 2;
		i++;
	}
	if (nb == 0)
		return (i);
	else
		return (0);
}
