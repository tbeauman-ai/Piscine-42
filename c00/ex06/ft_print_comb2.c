#include <unistd.h>

void	ft_print(int i, int j)
{
	char	s[7];

	s[0] = i / 10 + '0';
	s[1] = i % 10 + '0';
	s[2] = ' ';
	s[3] = j / 10 + '0';
	s[4] = j % 10 + '0';
	s[5] = ',';
	s[6] = ' ';
	write(1, s, 7);
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < 98)
	{
		while (j < 100)
		{
			if (i < j)
			{
				ft_print(i, j);
			}
			j++;
		}
		i++;
		j = i + 1;
	}
	write(1, "98 99", 5);
	return ;
}
