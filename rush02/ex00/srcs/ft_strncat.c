char	*ft_strncat(char *dest, char *src, int n)
{
	int	i;

	i = 0;
	while (dest[i])
		i++;
	while (*src && i < n)
	{
		dest[i] = *src;
		i++;
		src++;
	}
	dest[i] = 0;
	return (dest);
}
