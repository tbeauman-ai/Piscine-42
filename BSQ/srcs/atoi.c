#include "bsq.h"

unsigned int	ft_bsq_atoi(char *str)
{
	int	result;

	result = 0;
	while (*(str + 4) && (*str >= '0' && *str <= '9'))
		result = result * 10 + (*str++ - '0');
	return (result);
}
